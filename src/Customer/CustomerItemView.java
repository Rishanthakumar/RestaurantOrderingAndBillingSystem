    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
         
        if(!deleteNo.getText().isEmpty())
        {
            try{
                       int itmPac = Integer.parseInt(deleteNo.getText());
                       
                
                //call add order function inorder to add 
                      
                         cus1.deleteOrderItemOrPack(itmPac);
                         deleteNo.setText("");
                         setValues();
                
                
            
            }
            catch(Exception e){
            
             JOptionPane.showMessageDialog(null,"Please provide valid Item/Package number and quantity" );
            }
        
        }
        else
        {
           JOptionPane.showMessageDialog(null,"Provide valid Item/Package number! " ); 
        }
        


// TODO add your handling code here:
    }//GEN-LAST:event_deleteActionPerformed

