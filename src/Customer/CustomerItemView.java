    private void addToOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToOrderActionPerformed
       
        
       
        
        if(!itmOrpac.getText().isEmpty() && !qty.getText().isEmpty() )
        {
            try{
                       int itmPac = Integer.parseInt(itmOrpac.getText());
                       int qunt = Integer.parseInt(qty.getText());
                
                //call add order function inorder to add 
                      
                         cus1.addToOrder(itmPac,qunt);
                         itmOrpac.setText("");
                         qty.setText("");
                         setValues();
                
                
            
            }
            catch(Exception e){
            
             JOptionPane.showMessageDialog(null,"Please provide valid Item/Package number and quantity" );
            }
        
        }
        else
        {
           JOptionPane.showMessageDialog(null,"Please provide valid Item/Package number and quantity" ); 
        }
        
      
        

        // TODO add your handling code here:
    }//GEN-LAST:event_addToOrderActionPerformed

